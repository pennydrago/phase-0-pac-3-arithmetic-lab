// Addition function

function add(a, b) {
  return a + b;
}

// Subtraction function

function subtract(a, b) {
  return a - b;
}

// Multiplication function

function multiply(a, b) {
  return a * b;
}

// Division function

function divide(a, b) {
  return a / b;
}

// Function to increment by 1

function increment(n) {
  return n + 1;
}

// Function to decrement by 1

function decrement(n) {
  return n - 1;
}

// Function to parse string and return integer

function makeInt(n) {
  return parseInt(n, 10);
}

// Function to parse string and return float

function preserveDecimal(n) {
  return parseFloat(n);
}